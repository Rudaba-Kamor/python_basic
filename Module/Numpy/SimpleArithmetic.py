# ADDITION

import numpy as np

myarray1 = np.array([22, 34, 10, 50])
myarray2 = np.array([11, 45, 6, 30])

myarray3 = np.add(myarray1, myarray2)

print("Addition: " , myarray3)

# SUBTRACTION

import numpy as np

myarray1 = np.array([22, 34, 10, 50])
myarray2 = np.array([11, 45, 6, 30])

myarray3 = np.subtract(myarray1, myarray2)

print("Substraction: ",myarray3)

# Multiplication

import numpy as np

